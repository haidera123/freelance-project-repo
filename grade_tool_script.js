function filterSchoolsByKeyword(keyword) {
  keyword = String(keyword).trim(); // Convert to string and trim whitespace

  if (!keyword || keyword == "") {
    // If the keyword is empty, return all data
    return DATA;
  }

  const filteredData = DATA.filter((school) =>
    school.SCHOOLNAME.toLowerCase().includes(keyword.toLowerCase())
  );
  return filteredData;
}

function findSchoolIndex(schoolName) {
  let data = DATA;
  for (let i = 0; i < data.length; i++) {
    if (data[i].SCHOOLNAME.toUpperCase() === schoolName.toUpperCase()) {
      return i; // Found the school, return its index
    }
  }
  return -1; // School not found in the array
}
