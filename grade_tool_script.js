function downloadPdf(isBase64 = false) {
  if (!emailSent) {
    showError(
      "To download the PDF, please send the report to yourself via email first.",
      true
    );
    return;
  } else {
    showError("", false);
  }
  var doc = new jsPDF();
  doc.setFontSize(16);
  doc.text(6, 10, "Schools You Can Apply to");
  // Dummy arrays for table columns
  const safeSchools = matchingLawSchools.safeSchools;
  const reasonableSchools = matchingLawSchools.reasonableSchools;
  const reachSchools = matchingLawSchools.reachSchools;

  // Determine the maximum length of the arrays
  const maxLength = Math.max(
    safeSchools.length,
    reasonableSchools.length,
    reachSchools.length
  );

  const tableData = [];

  // Build the table data dynamically
  for (let i = 0; i < maxLength; i++) {
    const rowData = [];

    // Add row number (i + 1)
    rowData.push(i + 1);

    // Add values from each column if they exist, or an empty string if the column doesn't have a value
    if (i < safeSchools.length) rowData.push(safeSchools[i]);
    if (i < reasonableSchools.length) rowData.push(reasonableSchools[i]);
    if (i < reachSchools.length) rowData.push(reachSchools[i]);

    tableData.push(rowData);
  }

  doc.autoTable({
    theme: "striped",
    styles: {
      fontSize: 7,
    },
    startY: 20,
    head: [["#", "Safe Schools", "Reasonable Schools", "Reach Schools"]], // Add headers
    body: tableData,
  });
  if (isBase64) return doc.output("datauristring");
  else doc.save("School_report");
}
