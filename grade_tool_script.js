let matchingLawSchools = 0;

function categorizeLawSchools(data = DATA, targetScore) {
  const result = {
    safeSchools: [],
    reasonableSchools: [],
    reachSchools: [],
  };

  data.forEach((school) => {
    const compositeScore75 = school["Composite 75th"] * 1;
    const compositeScore50 = school["Composite 50th"] * 1;
    const compositeScore25 = school["Composite 25th"] * 1;

    if (targetScore >= compositeScore75) {
      if (NEVER_SAFE_SCHOOL.includes(school["SCHOOLNAME"]))
        result.reasonableSchools.push(school["SCHOOLNAME"]);
      else result.safeSchools.push(school["SCHOOLNAME"]);
    }
    if (targetScore < compositeScore75 && targetScore > compositeScore25) {
      result.reasonableSchools.push(school["SCHOOLNAME"]);
    }

    if (targetScore <= compositeScore25) {
      result.reachSchools.push(school["SCHOOLNAME"]);
    }
  });

  return result;
}

